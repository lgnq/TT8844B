void WaitVBlankVS1(unsigned char cnt)
{
	unsigned char i;
	unsigned int loop;

    tw884x_write(0xFF, 0x0);

	for (i = 0; i < cnt; i++) 
    {
		tw884x_write(0x13, 0xff);
		loop = 0;
		while (!(tw884x_read(0x13) & 0x04)) 
        {
			// wait VBlank
			loop++;
			if (loop > 0xFFFE) 
            {
				///Printf("\nERR:WaitVBlank VS1");
				break;
			}
		}		
	}
}

