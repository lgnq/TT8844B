void device_initialize(unsigned char *settings)
{
	unsigned char index, val;

	while ((settings[0] != 0xFF) || (settings[1] != 0xFF)) // 0xff, 0xff is end of data
    {           
		index = *settings;
		val   = *(settings+1);
        
		tw884x_write(index, val);
        
		settings += 2;
	}
}

